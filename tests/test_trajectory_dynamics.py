"""Tests for trajectory dynamics - velocity/derivative consistency.

This module tests that trajectories generated by pattern classes have:
1. Velocities that match the numerical derivative of positions
2. Continuous velocities without unreasonable jumps
3. Accelerations within reasonable bounds for telescope hardware
4. Continuous positions without unexpected discontinuities

These tests catch bugs like azimuth unwrapping issues where velocity
computation doesn't account for 0/360 degree wrap-around.
"""

